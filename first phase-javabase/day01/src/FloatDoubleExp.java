
public class FloatDoubleExp {
    public static void main(String[] args) {

        //案例1：定义圆周率并赋值为3.14，现有3个圆的半径分别为1.2、2.5、6，求它们的面积。
        double radius1 = 1.2;
        double radius2 = 2.5;
        double radius3 = 6.0;
        double PI = 3.14;
        System.out.println("圆的面积为分别为："+radius1*radius1*PI+","+radius2*radius2*PI+","+radius3*radius3*PI);

    }
}
